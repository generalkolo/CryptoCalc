public class sbdSteem {
    String sbd;
    String steem;
    String price;

    public String getSbd() {
        return sbd;
    }

    public void setSbd(String sbd) {
        this.sbd = sbd;
    }

    public String getSteem() {
        return steem;
    }

    public void setSteem(String steem) {
        this.steem = steem;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
